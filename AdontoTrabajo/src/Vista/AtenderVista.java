    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        // TODO add your handling code here:

        try {
            if (cliente != null) {
                throw new ClienteEnAtencionException();
            }
            Cliente cli = controlador.pasarAtencion();
            lblNombre2.setText("NOMBRE:    " + cli.getNombre());
            lblCedula2.setText("CEDULA:    " + cli.getCedula());
            lblEdad2.setText("EDAD:    " + cli.getEdad());
            btnAtender.setEnabled(true);
            btnTruno.setEnabled(true);
            cliente = cli;
            siguiente();

        } catch (ClienteEnAtencionException e) {
            e.getMessage();
        }

    }//GEN-LAST:event_btnSiguienteActionPerformed

    private void btnAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderActionPerformed
        // TODO add your handling code here:
        if (txtDiag.getText().isEmpty() || txtNombreD.getText().isEmpty()|| txtCedulaD.getText().isEmpty()|| txtRol.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "LLene los campos");
        } else {
            String diagnostico = txtDiag.getText();
            String cedulaD= txtCedulaD.getText();
            String nombre= txtNombreD.getText();
            String rol= txtRol.getText();
            Dentista dentista= new Dentista(nombre, rol, cedulaD);
            Atencion atencions = new Atencion(cliente, dentista, diagnostico);
            controlador.agregarHistorial(atencions);
            controlador.fechaAtencion(atencions);
            JOptionPane.showMessageDialog(null, "Paciente ready");
            limpiar();
            lblNombre2.setText("NOMBRE:");
            lblCedula2.setText("CEDULA:");
            lblEdad2.setText("EDAD:");
            btnAtender.setEnabled(false);
            btnTruno.setEnabled(false);
            cliente = null;
            siguiente();

        }


    }//GEN-LAST:event_btnAtenderActionPerformed

