    private void btnTrunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrunoActionPerformed
        // TODO add your handling code here:
        String nombre = cliente.getNombre();
        String cedula = cliente.getCedula();
        int edad = cliente.getEdad();
        Cliente clientes = new Cliente(cedula, nombre, edad);
        controCli.añadirCliente(clientes);
        JOptionPane.showMessageDialog(null, "Devuelta a la fila");
        lblNombre2.setText("NOMBRE:");
        lblCedula2.setText("CEDULA:");
        lblEdad2.setText("EDAD:");

        btnAtender.setEnabled(false);
        btnTruno.setEnabled(false);
        cliente=null;
        siguiente();


    }//GEN-LAST:event_btnTrunoActionPerformed
    public void limpiar() {
        txtNombreD.setText("");
        txtDiag.setText("");
        txtCedulaD.setText("");
        txtRol.setText("");
    }
    private void siguiente() {
        Cliente cliente = controlador.mostrarSiguiente();

        if (cliente == null) {
            lblNombre.setText("No hay pacientes");
            lblNombre.setText("");
            lblEdad.setText("");
            btnSiguiente.setEnabled(false);
            
            return;
        }

        lblNombre.setText("NOMBRE:    " + cliente.getNombre());
        lblNombre.setText("CEDULA:    " + cliente.getCedula());
        lblEdad.setText("EDAD:    " + cliente.getEdad());
        btnSiguiente.setEnabled(true);
        
    }

